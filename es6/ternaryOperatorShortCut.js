//Normal ifElse

let money = 80;
let food;

if (money > 100) {
  food = "Biryani";
} else {
  food = "Cha Biscuits";
}
console.log(food);

// ternary operator

let food1 = money > 100 ? "Biryani" : "Cha Biscuits";
console.log(food1);

let age = 18;
let people = age >= 18 ? "Eligible for VOTE" : "Not eligible for VOTE";
console.log(people);
